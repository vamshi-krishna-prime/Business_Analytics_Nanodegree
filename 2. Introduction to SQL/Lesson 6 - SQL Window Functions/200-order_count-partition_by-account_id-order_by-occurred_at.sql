WINDOW FUNCTIONS:

Order count of standard_qty, 'PARTITION BY' account_id and
'ORDER BY' occurred_at
*/

SELECT account_id,
       DATE_TRUNC('month', occurred_at),
       standard_qty,
       COUNT(standard_qty) OVER (PARTITION BY account_id ORDER BY occurred_at) AS order_count
FROM orders;

